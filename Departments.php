                    <p>The Ophthalmology residency is a three-year clinical training program. The residency program director is Dr. Ama Sadaka. We offer 1 residency position yearly for a total of 3 residents in the program at any one time. These positions are open to graduates of accredited medical schools who have completed at least one year of postgraduate medical training. The residency training takes place at LAU Medical Center-Rizk Hospital mainly, where residents rotate in different subspecialties and run their own outpatient continuity clinics. In addition, they cover the emergency room, operating room and inpatient service at LAU Medical Center-Rizk Hospital. They also rotate at Clemenceau Medical Center and at Howard Karagheusian Commemorative Corporation.</p>
