                    <p>Monitored Anesthesia Care means that an anesthesia provider will be with you throughout your procedures to provide you with sedative and pain medication. The amount of medication will be carefully measured, and depending on your needs, will result in anywhere from moderate to deep sedation. The end result is that your procedure can be performed without any anxiety or discomfort. During moderate sedation, you will usually be able to speak and respond to verbal cues throughout the procedure, communicating any discomfort you might experience to your anesthesia provider. During deep sedation, you will be completely unaware of your surroundings. Regardless of the depth of sedation required, your anesthesiologist will monitor your heart rate, blood pressure, breathing, and level of alertness closely throughout and after the procedure.</p>
